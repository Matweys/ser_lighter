# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º build-essential –¥–ª—è –∫–æ–º–ø–∏–ª—è—Ü–∏–∏ (–Ω—É–∂–Ω–æ –¥–ª—è TA-Lib)
