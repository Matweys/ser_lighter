# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º ta-lib –±–∏–±–ª–∏–æ—Ç–µ–∫—É (–Ω—É–∂–Ω–æ –¥–ª—è TA-Lib Python –ø–∞–∫–µ—Ç–∞)
